    public static void generateHTMLOption( String optionValue, String optionLabel, ServletRequest request,
                                           Writer writer, String parameterName )
        throws IOException
    {
        writer.write( "<option value=\"" );
        writer.write( optionValue );
        writer.write( "\"" );
        String parameterValue = request.getParameter( parameterName );
        if ( StringUtils.isEmpty( parameterValue ) )
        {
            parameterValue = (String) request.getAttribute( parameterName );
        }
        if ( optionValue.equals( parameterValue ) )
        {
            writer.write( " selected=\"true\" " );
        }
        writer.write( " >" );
        writer.write( optionLabel );
        writer.write( "</option>" );
    }

    public static void generateHTMLOption( String optionValue, ServletRequest request, Writer writer,
                                           String parameterName )
        throws IOException
    {
        generateHTMLOption( optionValue, optionValue, request, writer, parameterName );
    }

